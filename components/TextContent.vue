      <h3 class="title">
        <a id="chapter2-1"></a>
        2-1. 誰が開催しているのか
      </h3>
      <p>
        ルービックキューブの公式大会、および公式記録は、World Cube
        Association、通称WCAが取り仕切っています。
        日本語では、世界キューブ協会と呼びます。WCAは、2004年に発足して活動を開始し、
        2018年10月にはアメリカで非営利団体（NPO）として法人登録されました。
      </p>
      <p>
        各国には、WCAから権限を委譲された団体が存在します。日本でのその組織は、
        一般社団法人スピードキュービングジャパン、通称SCJ(Speedcubing
        Japan)といいます。
        2020年までは、前身となる日本ルービックキューブ協会(JRCA)がその組織でしたが、
        法人化にともない、新たにSCJを立ち上げ運営しています。
        SCJには、公式大会を開催できる人として、 WCA代理人（WCA
        Delegate）が所属しています。この代理人が大会の開催をWCAに宣言し、
        大会の運営を取り仕切り、結果をWCAに報告することで、公式記録として認定されます。
      </p>
      <p>
        また、SCJはWCA公式記録とは認定されない日本独自の大会として、SCJ大会の開催も行っています。
      </p>
      <h3 class="title">
        <a id="chapter2-2"></a>
        2-2. 大会に出るには
      </h3>
      <p>
        大会開催は、WCAやSCJのホームページで告知されます。
        すべての大会はWCAに専用ページが作成され、結果もそのページで公開されますが、
        日本での開催の場合企画はSCJが行っているため、基本的にはSCJからのほうが早く告知されます。
      </p>
      <p>
        大会への申し込みは、インターネット上で行います。SCJの専用ページで申し込むことが多いですが、
        大会によっては独自に専用ホームページや申し込みフォームを作成している場合もあります。
      </p>
