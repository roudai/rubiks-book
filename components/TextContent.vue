      </p>
      <p>
        公式競技の中で最も面数の多いパズルなので、とても難しいように見えますが、
        実際にはかなりの部分が3x3x3キューブの応用で解くことができます。
      </p>
      <p>
        また公式競技としてはありませんが、メガミンクスの派生パズルとして、
        各面が2層になったギガミンクス、3層になったテラミンクス、4層になったペタミンクスといったものも存在します。
      </p>
