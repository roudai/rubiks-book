      <h3 class="title">
        <a id="short4"></a>
        小話4 視覚障がい者とルービックキューブ
      </h3>
      <p>
        ルービックキューブは、実は全盲の視覚がい者の方も楽しむことができるパズルです。
        「色を揃える」ことを目的とするルービックキューブは、
        普通に考えると見ることができないことには遊ぶことが不可能なように思えます。
        確かに普通のルービックキューブでは色が揃っているのか揃っていないのか分からないので、
        遊ぶことはできません。ではどうするかというと、各面にそれぞれ手触りの違いをつけることで、
        ルービックキューブが揃っていることを表現します。
        それは例えば点字で表してもいいですし、
        もっと分かりやすいように面によってそれぞれ別の形にビーズを装飾することで面の違いを表現することもできます。
      </p>
      <p>
        WCAの公式ルールの中でも、「視覚障がいを持つ競技者は異なる面に異なる手触りがあるパズルを用いることができる」
        という明確なルールがあります。ただし、視覚障がい者にとっては色は関係なくても、
        スクランブラやジャッジが判断できるよう各面は異なる色がついていることが望ましいともされています。
        日本でも、実際に全盲で公式大会に出場されている方がいらっしゃいます。
      </p>
      <p>
        また、全盲ではないまでも、世界のトップレベルの選手の中にも色盲により色の区別が付きにくいという競技者もいます。
        こういった方は、自分が識別しやすいような色に自分のキューブをカスタマイズする等の工夫をしているようです。
      </p>
      <h3 class="title">
        <a id="chapter3-16"></a>
        3-16. 4x4x4 目隠し
      </h3>
      <p>
        4x4x4キューブを、目隠しをして揃える競技です。基本的なルールは、3x3x3目隠しと変わりません。
        Best of X（X=1～3）形式で順位を決めます。
        記憶量としては3x3x3キューブの約3倍になり、
        格段に難易度が上がるため3回連続で成功させることは容易ではありませんが、
        2019年から3x3x3目隠しと同様にMean of
        3によるランキング付けも開始されました。
      </p>
      <h3 class="title">
        <a id="chapter3-17"></a>
        3-17. 5x5x5 目隠し
      </h3>
      <p>
        5x5x5キューブを、目隠しをして揃える競技です。
        基本的なルールは、3x3x3目隠し、4x4x4目隠しと変わりません。 Best of
        X（X=1～3）形式で順位を決め、Mean of
        3によるランキング付けも2019年より開始されました。
        目隠し競技としては、公式競技の中で最大となります。記憶量は3x3x3キューブの約5倍になります。
      </p>
      <h3 class="title">
        <a id="chapter3-18"></a>
        3-18. 3x3x3 複数目隠し
      </h3>
      <p>
        3x3x3キューブを複数同時に記憶し、目隠しをして記憶したものをすべて揃える競技です。
        Best of X（X=1～3）形式で順位を決めます。
      </p>
      <p>
        挑戦するキューブの数によって制限時間が異なり、6個未満の場合はキューブの個数×10分（例：4個の場合40分）、
        6個以上の場合は一律して60分となります。世界記録は既に60個を超えているため、
        1つあたり1分以下で記憶と完成をこなしていることになります。
        過去には60分の上限がなく、5時間以上にも渡って競技を行っていたこともありますが、
        競技時間がかかりすぎるのと、記憶の要素が強すぎるとの理由で、60分の上限が決められました。
      </p>
      <p>
        完成されたパズルの個数から、未完成のパズルの個数を引いた結果から順位が決められます。
        つまり、10個挑戦して7個成功した場合は、7 - 3 = 4が結果となります。
        計算結果がゼロの場合（例：10個挑戦して5個成功）までは記録が認められますが、
        マイナスとなった場合（例：10個挑戦して4個成功）は記録なしとなります。
        ただし例外として、2個挑戦して1個成功した場合は記録なしとなります。
        結果個数が同じ競技者がいる場合は、よりタイムの早い人が順位が上になります。
      </p>
      <p>
        何十個ものキューブを記憶する姿は見た目のインパクトが大きく、
        テレビ等でもとりあげられることが多い競技です。
        5x5x5目隠し以上に、キューブの技術よりも記憶の技術に重点が置かれた競技になります。
      </p>
