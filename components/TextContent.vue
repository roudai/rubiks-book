      <h2 class="title">
        <a id="chapter2"></a>
        2. ルービックキューブの大会とは
      </h2>
      <p>
        テレビなどで、ルービックキューブの大会が開催されているのを見たことがある人はいるかと思いますが、
        具体的にどのようにして大会が行われているのかを知っている人は少ないのではないでしょうか。
