        まずは、ルービッククロックです。ルービッククロックは、公式競技として認められているパズルの中でも、
        最も特異なパズルと言って良いかと思います。
        なぜなら、これだけは「立体パズル」とは言い難いものだからです。
        ルービッククロックの見た目については下記ページを参考にしてください。<br />
        <a
          href="https://store.tribox.com/products/detail.php?product_id=3047"
          target="_blank"
          >https://store.tribox.com/products/detail.php?product_id=3047</a
        >
      </p>
      <p>
        ルービッククロックは4つのボタンと、4つのダイヤルを駆使して、裏表9つずつある針を、
        すべて同じ方向（時計で言う12時の方向）に揃えれば完成となるパズルです。
        見ての通り、見た目もパズルの内容も、ルービックキューブとは全く異なります。
      </p>
      <p>
        表裏両方を同時に揃える必要があり、一見するととても難しいパズルに見えますが、
        おそらくパズルとしては、公式競技で認められているものの中で最も簡単なものです。
        ルービックキューブを解き方を学ばず自力で解くのはかなり難しいかと思いますが、
        ルービッククロックは十分自力で解くことが可能なパズルだと思います。
      </p>
      <p>
        他のパズル同様、Average of 5形式で競技を行います。
        ルービッククロックは大会で行うには一つ大きな難点のある競技で、
        それは観客から見るとパズルを解いている過程が全く分からず、
        またパズルが完成したのかどうかも遠くからではよく分からないことです。
        パズルの完成を確認するには表裏を確認する必要があり、その確認はジャッジが行います。
      </p>
      <p>
        また、パズルの要素のひとつである「ピン」の状態もスクランブルとして含まれ、
        このピンは横倒しにすると動いてしまい状態が変わってしまうため、
        ジャッジや競技者は競技開始前にパズルを倒してしまわないよう細心の注意を払う必要があります。
      </p>
